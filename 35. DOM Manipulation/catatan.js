// DOM Manipulation

// Method Manipulasi Element
// - element.innerHTML | untuk mengubah isi dari sebuah tag yang diseleksi
// - element.style.<property> | untuk memberikan styling pada elemen yang diseleksi
// - element.setAttribute() | untuk menambahkan atribut
// - element.classList() | untuk mengelola class
//   - element.classList.add() | untuk menambahkan class baru
//   - element.classList.remove() | untuk menghapus class yang ada
//   - element.classList.toggle() | untuk mengetahui apakah element sudah memiliki class tertentu, jika belum maka ditambahkan, jika sudah maka dihilangkan
//   - element.classList.item() | untuk mengetahui class tertentu yang ada pada element
//   - element.classList.contains() | untuk mengecek sebuah elemen apakah memiliki class tertentu
//   - element.classList.replace() | untuk mengganti class yang ada dengan yang baru
// - ...


