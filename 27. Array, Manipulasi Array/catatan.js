// Array method
// - length, untuk mengetahui jumlah elemen yang ada pada array
// - join, yang menggabungkan seluruh isi array dan mengubahnya menjadi string
// - push, menambahkan satu atau lebih elemen ke akhir array
// - pop, menghapus elemen terakhir dari array
// - shift, menghapus elemen pertama dari array
// - unshift, menambahkan satu atau lebih elemen di awal array
// - slice, mengembalikan bagian tertentu dari array sebagai array baru tanpa mengubah array asli. Slice menerima dua argumen, yaitu indeks awal (inclusive) dan indeks akhir (exclusive).
// - splice, mengubah isi array dengan menghapus, mengganti, atau menambahkan elemen baru. Ini mengubah array asli.
// - foreach, digunakan untuk mengeksekusi fungsi tertentu pada setiap elemen dalam array.
// - map, menciptakan array baru dengan hasil pemanggilan fungsi tertentu pada setiap elemen array.
// - filter, menciptakan array baru yang berisi semua elemen dari array asli yang memenuhi kondisi yang ditentukan dalam fungsi.
// - find, mengembalikan nilai elemen pertama dalam array yang memenuhi kondisi yang ditentukan dalam fungsi. Jika tidak ada elemen yang memenuhi, undefined akan dikembalikan.